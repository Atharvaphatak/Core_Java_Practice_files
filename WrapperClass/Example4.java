import java.util.*;
class Example4
{
	public static void main(String args[])
	{
		Float f1=new Float(100.5f);//float
		Float f2=new Float(200.5d);//double
		Float f3=new Float("300.5f");
		System.out.println(f1);		//value
		System.out.println(f2);	
		System.out.println(f3);	
	}
}
/*
100.5
200.5
300.5
*/




