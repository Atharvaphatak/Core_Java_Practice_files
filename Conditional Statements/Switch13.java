class Switch13
{
public static void main(String[] args)
{
	String s1="cyber";
	switch(s1)
	{ 
	case "cyber":System.out.println("Statement One");
				break;
	case "success":System.out.println("Statement Two");
				break;
	default :System.out.println("Statement Default..");
				break;
	}
}
}

	
	
	
	
