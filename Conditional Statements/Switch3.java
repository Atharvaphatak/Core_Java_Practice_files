class Switch3
{
public static void main(String[] args)
{
	int a=5;
	switch(a)
	{ 
	case 1: System.out.println("Statement One");
	break;
	case 2: System.out.println("Statement Two");
	break;
	case 3: System.out.println("Statement Three");
	break;
	default : System.out.println("Statement Default..");
	break;
	}
}
}

	
	
	
	
