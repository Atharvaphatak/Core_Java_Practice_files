class Forloop2
{
	public static void main(String[] args)
	{
		int i=3;
		System.out.println(i);//3
		//pre decrement
		//firstly decrement i value by 1 and 
		//secondly it will print..
		System.out.println(--i);//2
		System.out.println(i);//2
		
		//Post decrement
		//firstly it will print.. and 
		//secondly decrement i value by 1
		System.out.println(i--);//2
		System.out.println(i);//1
	}
}
