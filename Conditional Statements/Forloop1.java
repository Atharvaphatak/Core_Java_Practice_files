class Forloop1
{
	public static void main(String[] args)
	{
	int i=1;
	System.out.println(i);//1
	//pre increment
	//firstly increment i value by 1 and 
	//secondly it will print..
	System.out.println(++i);//2
	System.out.println(i);//2
	
	//Post increment
	//firstly it will print.. and 
	//secondly increment i value by 1
	System.out.println(i++);//2
	System.out.println(i);//3
	}
}
