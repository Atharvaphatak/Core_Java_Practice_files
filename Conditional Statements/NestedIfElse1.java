class NestedIfElse1
{ 
	public static void main(String args[])
	{
		int Temp=45;
		boolean TicketStatus=true;
		if(Temp>40)
		{
			if(TicketStatus=true)
			{
				System.out.println("Travel to Kashmir");
			}
			else
			{
				System.out.println("Do not Travel to Kashmir");
			}
		}
		else
		{
			if(TicketStatus=true)
			{
				System.out.println("Travel to Shimla");
			}
			else
			{
				System.out.println("Do not Travel to Shimla");
			}	
		}
	}		
}
		
		
		
		