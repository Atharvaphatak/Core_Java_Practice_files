class Staticvar2
{	static int a;
	static float b;
	static long c;
	static char d;
	static boolean e;
	static byte f;
	static short g;
	static double h;
	//it is not mandatory to initialize static variable before printing.
	public static void main(String args[])
	{
		//access directly..
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);	
		System.out.println(f);	
		System.out.println(g);	
		System.out.println(h);	
	}
}
